package main

import "fmt"

func main() {
	fmt.Println("valor1 == valor2: Retorna TRUE si valor1 y valor2 son exactamente iguales.")
	fmt.Println("valor1 != valor2: Retorna TRUE si valor1 es diferente de valor2.")
	fmt.Println("valor1 < valor2: Retorna TRUE si valor1 es menor que valor2")
	fmt.Println("valor1 > valor2: Retorna TRUE si valor1 es mayor que valor2")
	fmt.Println("valor1 >= valor2: Retorna TRUE si valor1 es igual o mayor que valor2")
	fmt.Println("valor1 <= valor2: Retorna TRUE si valor1 es menor o igual que valor2.")
}
